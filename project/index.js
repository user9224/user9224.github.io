var currentTab = 'introduce';
function CreateTabButton(id, name, isActive) {
    var button = document.createElement('button');
    button.className = 'tab-button';
    button.innerHTML = "".concat(name);
    button.dataset.active = "".concat(isActive);
    button.onclick = function (ev) {
        console.log(ev);
        currentTab = id;
        app();
    };
    return button;
}
function RenderTabs(currentTab) {
    var tabs = document.querySelector('.tabs');
    if (!tabs) {
        return;
    }
    tabs.innerHTML = "";
    for (var _i = 0, _a = Object.keys(Tabs); _i < _a.length; _i++) {
        var tabId = _a[_i];
        var button = CreateTabButton(tabId, Tabs[tabId].title, tabId === currentTab);
        tabs.appendChild(button);
    }
}
function RenderTabName(currentTab) {
    var tabName = document.querySelector('.navigation .name');
    console.log(tabName);
    if (!tabName) {
        return;
    }
    tabName.innerHTML = "".concat(Tabs[currentTab].title);
}
function RenderContents(currentTab) {
    var content = document.querySelector('.content');
    if (!content) {
        return;
    }
    content.innerHTML = "";
    var tab = Tabs[currentTab];
    content.innerHTML = "<h1>".concat(tab.title, "</h1>").concat(tab.content);
}
function app() {
    RenderTabs(currentTab);
    RenderTabName(currentTab);
    RenderContents(currentTab);
}
document.addEventListener('DOMContentLoaded', app);
